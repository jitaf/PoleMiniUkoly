public class B2Student {
    private String jmeno;
    private double prumer;

    public B2Student(String jmeno, double prumer) {
        this.jmeno = jmeno;
        this.prumer = prumer;
    }

    public String getJmeno() {
        return jmeno;
    }

    public double getPrumer() {
        return prumer;
    }
}
