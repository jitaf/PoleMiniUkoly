public class B3Autobus {
    private int kapacita;
    private int cestujici;

    public B3Autobus(int kapacita, int cestujici) {
        this.kapacita = kapacita;
        this.cestujici = cestujici;
    }

    public int getKapacita() {
        return kapacita;
    }

    public int getCestujici() {
        return cestujici;
    }
}
